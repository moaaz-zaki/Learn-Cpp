#include "calculate.h"

    int calculate::add (int num1 , int num2 )
    {
        return num1 + num2;
    }

    int calculate::add (int num1 , int num2 , int num3)
    {
        return num1 + num2 + num3;
    }

    float calculate::add (float num1 , float num2 )
    {
        return num1 + num2;
    }

    string calculate::add (string name1 , string name2)
    {  
        return name1 + " " + name2;
    }